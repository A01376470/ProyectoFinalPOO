    private void showVehiculos(Vehiculo[] vehiculos){
        javax.swing.JPanel view = (javax.swing.JPanel)ListaVehiculos.getViewport().getView();
        view.removeAll();
        for(Vehiculo vehiculo: vehiculos){
            if(vehiculo != null){
                javax.swing.JLabel label = new javax.swing.JLabel();
                label.setText(vehiculo.toString());
                view.add(label);
            }
        }
        
        ListaVehiculos.revalidate();
        ListaVehiculos.repaint();
    }

