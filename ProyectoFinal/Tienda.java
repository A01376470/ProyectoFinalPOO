    private void RealizarButtonActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_RealizarButtonActionPerformed
    if (MarcaTextField.getText().equals("Marca") && ColorTextField.getText().equals("Color")){  
        ExcepcionesLabel.setText("Favor de llenar todos los campos");
    }  else{
        if(VenderToggle.isSelected()){
            try{
                inventario.venderVehiculo(Integer.parseInt(ModeloTextField.getText()), MarcaTextField.getText(), ColorTextField.getText(), Double.parseDouble(PrecioTextField.getText()));
                showVehiculos(inventario.getVehiculos());
                ExcepcionesLabel.setText("Vehículo vendido exitosamente");
                ModeloTextField.setText("Modelo (año)");
                PrecioTextField.setText("Precio (en miles de pesos)");
                ColorTextField.setText("Color");
                MarcaTextField.setText("Marca");
            } catch (NumberFormatException nf) {
                ExcepcionesLabel.setText("Favor de llenar todos los campos");
            } catch (EmptyInventoryException e){
                ExcepcionesLabel.setText(e.getMessage());
            } catch (IllegalArgumentException i){
                ExcepcionesLabel.setText("El vehículo no se encuentra en el inventario");
            } catch (NullPointerException n){
                ExcepcionesLabel.setText("El vehículo no existe");
            } 
        } else if (ComprarToggle.isSelected()){
            String tipo = (String)VehiculoBox.getSelectedItem();
            try{
                inventario.comprarVehiculo(tipo, Integer.parseInt(ModeloTextField.getText()), MarcaTextField.getText(), ColorTextField.getText(), Double.parseDouble(PrecioTextField.getText()));
                showVehiculos(inventario.getVehiculos());
                ExcepcionesLabel.setText("Vehículo comprado exitosamente");
                ModeloTextField.setText("Modelo (año)");
                PrecioTextField.setText("Precio (en miles de pesos)");
                ColorTextField.setText("Color");
                MarcaTextField.setText("Marca");
            } catch (NumberFormatException nf) {
                ExcepcionesLabel.setText("Favor de llenar todos los campos");
            } catch (FullInventoryException e){
                ExcepcionesLabel.setText(e.getMessage());
            } catch (IllegalArgumentException i){
                ExcepcionesLabel.setText("El modelo tiene que estar en el rango 1970-2019");
            } catch (NullPointerException n){
                ExcepcionesLabel.setText("El vehículo no existe");
            } 

        } else {
            ExcepcionesLabel.setText("Favor de seleccionar si desea comprar o vender");
        }
        
    
    }
    }//GEN-LAST:event_RealizarButtonActionPerformed

